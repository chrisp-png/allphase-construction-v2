<h2>HVHZ Requirements When Replacing Your Roof</h2>
<p><strong>When you replace a roof in Broward or Palm Beach Counties, you must meet current HVHZ codes.</strong></p>
<p>This means your new roof must:</p>
<h3>1. Meet 180mph Wind Load Requirements</h3>
<ul>
<li>Enhanced fastening (6-8 nails per shingle, vs 4 standard)</li>
<li>Proper spacing and placement</li>
<li>Upgraded underlayment (synthetic required in most areas)</li>
<li>Sealed edges and perimeters</li>
</ul>
<h3>2. Include Proper Underlayment</h3>
<ul>
<li><strong>Self-adhering underlayment</strong> in critical areas (valleys, edges, penetrations)</li>
<li><strong>Synthetic underlayment</strong> for main roof areas</li>
<li><strong>Minimum 30-lb felt</strong> in older code (rarely allowed now)</li>
</ul>
<h3>3. Use Approved Materials</h3>
<ul>
<li><strong>Shingles:</strong> Must be rated for HVHZ (not all shingles qualify)</li>
<li><strong>Tiles:</strong> Must use foam adhesive attachment, not just mortar</li>
<li><strong>Metal:</strong> Must meet Miami-Dade NOA specifications</li>
<li><strong>Flat:</strong> Must be mechanically fastened, not just adhered</li>
</ul>
<h3>4. Pass Required Inspections</h3>
<ul>
<li><strong>Deck inspection</strong> — Before any roofing installed</li>
<li><strong>Underlayment inspection</strong> — Before shingles/tiles</li>
<li><strong>Fastening inspection</strong> — During installation</li>
<li><strong>Final inspection</strong> — After completion</li>
</ul>
<p><strong>You cannot replace &quot;half a roof&quot; or &quot;section by section&quot; to avoid permits</strong> — this is illegal and voids insurance.</p>
