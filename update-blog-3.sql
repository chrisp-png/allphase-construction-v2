<h2>Should You Repair or Replace?</h2>
<p><strong>This is the million-dollar question.</strong></p>
<h3>When Repair Makes Sense</h3>
<p>✓ <strong>Roof is under 10 years old</strong> (shingles) or 20 years old (tile/metal)</p>
<p>✓ <strong>Damage is localized</strong> — Small section, not widespread</p>
<p>✓ <strong>Structural deck is sound</strong> — No rot or sagging</p>
<p>✓ <strong>Insurance will cover repairs</strong> — Claim approved</p>
<p>✓ <strong>No plans to sell soon</strong> — 5+ years of ownership planned</p>
<h3>When Replacement is Required</h3>
