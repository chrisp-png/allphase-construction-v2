<h2>Day 1: Tear-Off and Deck Inspection</h2>
<p><strong>This is the noisiest, messiest day of the project.</strong></p>
<h3>Morning (7:00 AM - 12:00 PM)</h3>
<p><strong>Dumpster delivery and property protection:</strong></p>
<ul>
<li>Dumpster placed in driveway or street (permit required for street placement)</li>
<li>Tarps and plywood laid around house perimeter</li>
<li>Gutters covered or removed temporarily</li>
<li>HVAC units and outdoor equipment protected</li>
</ul>
<p><strong>Tear-off begins:</strong></p>
<ul>
<li>Workers remove existing shingles, tiles, or metal panels</li>
<li>Old underlayment and felt paper removed</li>
<li>Debris thrown directly into dumpster</li>
<li>Roof deck exposed for inspection</li>
</ul>
<p><strong>What it sounds like:</strong> Constant scraping, tearing, and impact sounds. Like someone is walking on your ceiling with steel boots while ripping up flooring. It&#39;s LOUD.</p>
