<li><strong>Insurance coordination</strong> — If claim-related, approval before work begins</li>
</ul>
<p><strong>Red flag: Contractors who suggest skipping permits to &quot;save money&quot; are putting your insurance, resale value, and safety at risk.</strong></p>
<h3>Material Ordering (1-2 Weeks Before)</h3>
<p>Once permit is approved:</p>
<ul>
<li>Roofing materials ordered (shingles, tiles, metal panels)</li>
<li>Underlayment and supplies delivered to warehouse</li>
<li>Dumpster scheduled for delivery</li>
<li>Crew assigned and schedule confirmed</li>
</ul>
