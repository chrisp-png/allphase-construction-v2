<h3>Tile Roofs</h3>
<p><strong>Typical timeline: 3-5 days</strong></p>
<ul>
<li><strong>Day 1:</strong> Tear-off, deck inspection</li>
<li><strong>Day 2:</strong> Underlayment, batten installation</li>
<li><strong>Day 3-4:</strong> Tile installation (slower than shingles)</li>
<li><strong>Day 5:</strong> Completion, cleanup</li>
</ul>
<p><strong>Why longer:</strong> Each tile must be individually placed and secured with foam adhesive + fasteners. Much slower than nailing shingles.</p>
<h3>Metal Roofs</h3>
<p><strong>Typical timeline: 3-7 days</strong></p>
<ul>
<li><strong>Day 1:</strong> Tear-off, deck inspection</li>
<li><strong>Day 2:</strong> Underlayment, clip system installation</li>
<li><strong>Day 3-5:</strong> Panel placement and seaming</li>
<li><strong>Day 6-7:</strong> Trim, flashing, completion</li>
</ul>
<p><strong>Why longer:</strong> Metal panels require precise measurement, cutting, and mechanical seaming. Standing seam roofs take longer than screw-down panels.</p>
<h3>Flat Roofs</h3>
<p><strong>Typical timeline: 2-4 days</strong></p>
<ul>
<li><strong>Day 1:</strong> Tear-off, deck inspection</li>
<li><strong>Day 2:</strong> Insulation, TPO/PVC membrane installation</li>
<li><strong>Day 3-4:</strong> Seam welding, flashing, completion</li>
</ul>
