<h2>HVHZ Inspection Requirements: What Makes South Florida Different</h2>
<p><strong>Every roof in Broward and Palm Beach Counties must meet 180mph wind standards.</strong></p>
<h3>Required Inspections</h3>
<p><strong>1. Deck Inspection (Day 1)</strong></p>
<p>Inspector verifies:</p>
<ul>
<li>Deck thickness (minimum 7/16&quot; OSB or 1/2&quot; plywood)</li>
<li>Fastener spacing (6&quot; edges, 12&quot; field)</li>
<li>Solid deck with no gaps</li>
<li>Proper deck attachment to trusses/rafters</li>
</ul>
<p><strong>2. Underlayment Inspection (Day 2)</strong></p>
<p>Inspector verifies:</p>
<ul>
<li>HVHZ-approved underlayment used</li>
<li>Proper overlap (6&quot; side, 4&quot; end)</li>
<li>Fastening pattern (staples or nails every 12&quot;)</li>
<li>All penetrations sealed</li>
<li>Valleys double-layered</li>
</ul>
<p><strong>3. Nailing Inspection (Day 2-3)</strong></p>
<p>Inspector verifies:</p>
<ul>
<li>Enhanced fastening (6-8 nails per shingle)</li>
<li>Nail placement in &quot;nailing zone&quot;</li>
<li>Perimeter enhanced fastening (3 rows)</li>
<li>Hip/ridge enhanced fastening</li>
<li>No missed areas</li>
</ul>
<p><strong>4. Final Inspection (Day 4-5)</strong></p>
<p>Inspector verifies:</p>
<ul>
<li>All work complete per plans</li>
<li>Proper flashing at all penetrations</li>
<li>Adequate ventilation</li>
<li>No visible defects</li>
<li>Certificate of Completion issued</li>
</ul>
<h3>Wind Mitigation Form OIR-B1-1802</h3>
<p><strong>Your contractor should provide:</strong></p>
<ul>
<li>Completed wind mitigation form</li>
<li>Roof-to-wall connection documentation</li>
<li>Secondary water resistance documentation</li>
<li>Opening protection documentation (if applicable)</li>
</ul>
<p><strong>Value:</strong> Submit to insurance company for discounts of 10-45% on premiums.</p>
