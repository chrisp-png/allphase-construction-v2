/**
 * Get all indexable routes for XML sitemap generation
 */
export function getIndexableRoutes(): SitemapEntry[] {
  return sheetSitemap.filter(entry => entry.indexable);
}

/**
 * Get routes by section name
 */
export function getRoutesBySection(sectionName: string): SitemapEntry[] {
  return sheetSitemap.filter(entry => entry.section === sectionName);
}

/**
 * Get all unique sections in order of appearance
 */
export function getAllSections(): string[] {
  const sections = new Set<string>();
  sheetSitemap.forEach(entry => sections.add(entry.section));
  return Array.from(sections);
}
