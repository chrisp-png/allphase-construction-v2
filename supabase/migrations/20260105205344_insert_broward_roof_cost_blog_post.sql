<p><strong>Truss Repairs or Reinforcement:</strong> If your roof inspection reveals damaged, undersized, or improperly spaced trusses, these structural repairs require general contractor authority.</p>

<p><strong>Fascia and Soffit Replacement:</strong> While often done during a reroof, replacing structural fascia boards or repairing soffit framing can constitute structural work requiring a CGC license.</p>

