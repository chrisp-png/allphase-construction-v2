  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Why Metal Makes Sense in Florida</h3>

  <p><strong>Built for hurricanes.</strong> Standing seam metal roofs are rated for 140-180 mph winds. They don''t have individual pieces that can tear off—the interlocking panels stay put. After Hurricane Ian, we saw entire neighborhoods of shingle roofs destroyed while metal roofs on the same street had zero damage.</p>

  <p><strong>Insurance discounts.</strong> This is the big one Florida homeowners miss. Many insurers offer <strong>15-35% premium discounts</strong> for metal or other impact-resistant roofing. On a $4,000/year policy, that''s $600-$1,400 saved annually—which adds up to $18,000-$42,000 over the roof''s lifetime.</p>

  <p><strong>Energy efficiency.</strong> Metal reflects solar heat instead of absorbing it. In a state where AC accounts for 40%+ of your electric bill, a 20-25% reduction in cooling costs is real money. On average, Florida homeowners save $300-$600/year on electricity with metal roofing.</p>

  <p><strong>Longevity.</strong> A metal roof installed today will likely be the last roof you ever buy for this house. At 50+ years, you''re looking at one roof versus 2-3 shingle replacements over the same period.</p>

  <p><strong>No algae or mold.</strong> Metal doesn''t support organic growth. No more black streaks, no pressure washing, no algae-resistant treatments.</p>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">The Downsides to Consider</h3>

  <p><strong>Higher upfront cost.</strong> There''s no getting around it—you''ll pay roughly double what shingles cost. If cash flow is a concern, this matters.</p>

  <p><strong>Noise during rain.</strong> Florida gets heavy afternoon storms. Metal roofs are louder than shingles during rain, though proper underlayment and insulation minimize this significantly. Most homeowners tell me they stop noticing after a few weeks.</p>

  <p><strong>Denting from hail.</strong> While rare in South Florida, hail can dent some metal panels. Aluminum dents more easily than steel. Stone-coated steel offers the best impact resistance.</p>

  <p><strong>Fewer contractors.</strong> Not every roofer installs metal properly. You''ll want a contractor with specific metal roofing experience and manufacturer certifications.</p>

