  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Why Florida Homeowners Choose Shingles</h3>

  <p><strong>Lower upfront cost.</strong> If your budget is tight or you''re selling within 5-7 years, shingles make financial sense. You''ll pay roughly half what metal costs today.</p>

  <p><strong>Easy repairs.</strong> When a branch punches through or you lose a few shingles in a storm, repairs are straightforward and affordable ($150-$500 typically).</p>

  <p><strong>Familiar to every roofer.</strong> You''ll have no shortage of contractors who can install, inspect, and repair asphalt shingles.</p>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">The Downsides in Florida''s Climate</h3>

  <p><strong>Shorter lifespan.</strong> The combination of 95°F attic temperatures, daily UV bombardment, and humidity degrades shingles faster here than anywhere else in the country. Expect 15-20 years max from architectural shingles—not the 25-30 years manufacturers advertise for temperate climates.</p>

  <p><strong>Hurricane vulnerability.</strong> Standard shingles are rated for 60-80 mph winds. Even "high wind" shingles top out around 110-130 mph. After a major storm, shingle roofs often need significant repairs or full replacement.</p>

  <p><strong>Higher cooling costs.</strong> Dark shingles absorb heat. In Florida''s summer, that means your AC works overtime. We''ve seen customers'' cooling bills drop 20-25% after switching to reflective metal.</p>

  <p><strong>More maintenance.</strong> Algae, mold, and moss love Florida''s humidity. You''ll need periodic cleaning or algae-resistant treatments to keep shingles looking decent.</p>

