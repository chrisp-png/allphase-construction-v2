  <h2 class="text-3xl font-bold text-white mt-12 mb-6">Florida-Specific Considerations</h2>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Hurricane Preparedness</h3>

  <p>Florida Building Code requires roofs to meet specific wind resistance standards based on your location. Miami-Dade County has the strictest requirements in the nation.</p>

  <ul class="list-disc pl-6 space-y-2 text-gray-300 mb-6">
    <li><strong class="text-white">Shingles:</strong> Must be rated for your wind zone (typically 110-150 mph in South Florida). Requires specific nail patterns and may need additional adhesive strips.</li>
    <li><strong class="text-white">Metal:</strong> Standing seam exceeds most Florida wind requirements with ratings up to 180 mph. Fewer potential failure points.</li>
  </ul>

  <p>After 2024''s hurricane season, we''ve seen a significant shift toward metal—homeowners are tired of the stress and expense of post-storm repairs.</p>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Insurance Reality Check</h3>

  <p>Call your insurance agent BEFORE deciding. Ask specifically:</p>

  <ol class="list-decimal pl-6 space-y-2 text-gray-300 mb-6">
    <li>What discount do you offer for metal roofing?</li>
    <li>What discount do you offer for impact-resistant shingles?</li>
    <li>What documentation/certification do you need?</li>
  </ol>

  <p>Some insurers offer substantial discounts (25-35%), while others offer minimal savings. Get the actual numbers for your policy.</p>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">HOA Restrictions</h3>

  <p>Some Florida HOAs restrict roofing materials or colors. Check your covenants before committing to metal. Stone-coated steel tiles can be a good compromise—they offer metal durability with a traditional tile appearance.</p>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Coastal Corrosion</h3>

  <p>If you''re within a few miles of the ocean, salt air corrodes standard steel within 10-15 years. Options:</p>

  <ul class="list-disc pl-6 space-y-2 text-gray-300 mb-6">
    <li><strong class="text-white">Aluminum</strong> (corrosion-proof, but dents more easily)</li>
    <li><strong class="text-white">Galvalume steel</strong> with coastal-grade coating</li>
    <li><strong class="text-white">Stainless steel</strong> (premium cost but virtually indestructible)</li>
  </ul>

  <p>We always recommend aluminum for homes within 3,000 feet of saltwater.</p>

  <h2 class="text-3xl font-bold text-white mt-12 mb-6">When to Choose Shingles vs. Metal</h2>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Choose Shingles If:</h3>

  <ul class="list-disc pl-6 space-y-2 text-gray-300 mb-6">
    <li>Your budget is under $15,000</li>
    <li>You''re selling within 5-7 years</li>
    <li>Your HOA restricts metal roofing</li>
    <li>You have a complex roof with many dormers/valleys (metal costs escalate quickly on complex roofs)</li>
  </ul>

  <h3 class="text-2xl font-bold text-white mt-8 mb-4">Choose Metal If:</h3>

  <ul class="list-disc pl-6 space-y-2 text-gray-300 mb-6">
    <li>You''re staying 10+ years</li>
    <li>Hurricane protection is a priority</li>
    <li>You want to maximize insurance discounts</li>
    <li>You''re tired of frequent roof maintenance</li>
    <li>You''re near the coast (with aluminum)</li>
    <li>Your roof has a simple design (gable or hip)</li>
