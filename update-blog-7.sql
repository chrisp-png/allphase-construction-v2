<h2>Frequently Asked Questions</h2>
<h3>Why would a roofing contractor ask me to pull my own permit in Florida?</h3>
<p>A contractor asking you to pull your own roofing permit is the biggest red flag in the industry. They&#39;re asking because they&#39;re either unlicensed (illegal in Florida) or trying to avoid responsibility for their work. Florida law requires roofing contractors to hold a CCC (Certified Roofing Contractor) or CGC (Certified General Contractor) license, and licensed contractors must pull permits for work they perform. When you pull the permit instead of the contractor, you become the legal &quot;contractor of record&quot; and accept full responsibility for code compliance, proper installation, inspection coordination, and correction of any violations. The contractor doing the work has zero legal accountability to the building department. This means when work fails inspection (which it usually does with scam contractors), you&#39;re stuck coordinating corrections, paying extra, or hiring another contractor to fix problems. Legitimate contractors always pull their own permits because they&#39;re licensed, insured, and stand behind their work. Any contractor pressuring you to pull the permit is waving a massive red flag — end the conversation immediately and hire a properly licensed professional.</p>
<h3>What happens if roofing work fails inspection when I pulled the permit?</h3>
<p>When roofing work fails inspection under a homeowner-pulled permit, you become solely responsible for all corrections, costs, and consequences. The building department issues a stop-work order preventing further installation until violations are corrected, and sends a violation notice to you as the permit holder with a required timeline for fixes. At this point, contractors typically respond in three ways: demand extra payment to fix issues they caused, argue the inspector is wrong and refuse to fix anything, or simply disappear and stop answering calls. You&#39;re left with limited options: beg the original contractor to fix problems (rarely successful), hire a second contractor to correct another contractor&#39;s work (expensive and difficult to find someone willing), attempt repairs yourself (dangerous and unlikely to pass re-inspection), or live with an open failed permit (illegal, creates insurance problems, cannot sell property). Meanwhile, your roof may be exposed to weather with only partial protection, you&#39;re accumulating costs for work done twice, manufacturer warranties are void because installation wasn&#39;t by a licensed contractor, and insurance may deny future claims for improperly permitted work. Licensed contractors pass inspections because they know the code, have proper materials, and install correctly — homeowners pulling permits have none of these advantages.</p>
<h3>Does homeowner&#39;s insurance cover roofing work if I pulled my own permit?</h3>
<p>No, homeowner insurance typically excludes coverage for work you perform as a &quot;contractor,&quot; and pulling your own roofing permit legally designates you as the contractor of record. Standard homeowner&#39;s policies cover slip-and-fall accidents, property damage, and personal liability, but specifically exclude commercial activities and work performed in a contractor capacity. When you pull the roofing permit, you&#39;re engaging in activity your policy was never designed to cover. This creates multiple insurance problems: hurricane damage claims may be denied because the roof wasn&#39;t properly permitted under a licensed contractor, workmanship defects aren&#39;t covered because you were the legal &quot;installer,&quot; worker injury lawsuits can pierce through to you personally if the contractor lacked workers&#39; compensation insurance, and your insurance company may cancel your policy entirely upon discovering improperly permitted work. Additionally, manufacturer warranties require installation by licensed contractors and void when homeowners pull permits, meaning when the roof fails prematurely you&#39;re paying 100% replacement cost out of pocket with no insurance or warranty protection. The small amount you &quot;save&quot; by pulling your own permit exposes you to hundreds of thousands in uninsured liability. Always insist that licensed contractors pull their own permits to maintain full insurance and warranty protection.</p>
<h3>Are HVHZ roofing permits more complicated in Broward and Palm Beach Counties?</h3>
<p>Yes, High Velocity Hurricane Zone (HVHZ) roofing permits in Broward and Palm Beach Counties are significantly more complex than standard permits, requiring extensive documentation that homeowners are completely unqualified to prepare. HVHZ permits require Florida Product Approval numbers (NOAs) for every single material — shingles, underlayment, flashings, fasteners, and adhesives — plus manufacturer installation instructions with detailed fastener patterns and spacing, engineering calculations proving 180mph wind resistance for the specific property address, contractor licensing documentation (CCC or CGC license, general liability insurance, workers&#39; compensation insurance), and detailed scope of work including all HVHZ-specific requirements. The inspection process includes three mandatory inspections (deck, dry-in, final) with strict pass/fail criteria for HVHZ standards, photo documentation requirements for building department records, and extremely thorough enforcement by inspectors trained specifically for hurricane zone compliance. Each inspection verifies proper fastener spacing (6&quot; on edges, 12&quot; in field minimum), correct product installation per NOA specifications, enhanced fastening in perimeter zones for uplift resistance, and proper flashing details meeting 180mph wind standards. Licensed roofing contractors deal with this documentation and inspection process daily — homeowners have no idea where to start and will inevitably fail inspections, creating costly delays and correction requirements.</p>
<h3>How do I verify a roofing contractor is properly licensed in Florida?</h3>
<p>Verify a roofing contractor&#39;s license through the Florida Department of Business &amp; Professional Regulation website at myfloridalicense.com/CheckLicenseStatus before signing any contract or making payments. Search by the contractor&#39;s name or license number and verify they hold either a CCC (Certified Roofing Contractor) or CGC (Certified General Contractor) license, the license status shows &quot;Current&quot; or &quot;Active&quot; with an expiration date in the future, their bond and insurance are active and up-to-date, and check for any disciplinary actions, complaints, or violations on their record. In person, request written copies of their current contractor license (CCC or CGC), certificate of general liability insurance ($1M minimum coverage), certificate of workers&#39; compensation insurance (or valid exemption if they&#39;re a sole proprietor with no employees), and confirmation in writing that they will pull all required permits under their license. Red flags requiring you to walk away immediately include contractor refusing to provide license number, license searches returning &quot;No records found&quot; or showing expired/inactive status, contractor claiming they &quot;work under someone else&#39;s license&quot; (illegal in Florida), contractor asking you to pull the permit (massive warning sign of unlicensed operation), or contractor having multiple recent violations or unresolved complaints. Keep copies of all documentation for warranty registration, insurance purposes, and potential future claims. Professional contractors are proud of their credentials and happy to provide verification — scammers make excuses or become defensive.</p>
