<h2>The Inspection Process: Why Homeowners Fail</h2>
<p><strong>HVHZ roofing requires three inspections, each with specific pass/fail criteria.</strong></p>
<h3>Inspection 1: Deck Inspection</h3>
<p><strong>Occurs after tear-off, before any new materials installed.</strong></p>
<p><strong>Inspector verifies:</strong></p>
<p>✓ Roof deck is sound (no rot, delamination, or structural damage)</p>
<p>✓ Proper decking material installed (minimum 7/16&quot; OSB or 1/2&quot; plywood)</p>
<p>✓ Correct fastener spacing (6&quot; on edges, 12&quot; in field for HVHZ)</p>
<p>✓ H-clips installed between trusses (if required)</p>
<p>✓ Proper deck attachment to structure</p>
<p>✓ Any repairs meet code requirements</p>
<p><strong>Common failures:</strong></p>
<p>✗ Insufficient deck fasteners (doesn&#39;t meet HVHZ requirements)</p>
<p>✗ Wrong decking material or thickness</p>
<p>✗ Damaged deck not properly repaired</p>
<p>✗ Missing H-clips in required locations</p>
<p><strong>If you pulled the permit:</strong> You&#39;re responsible for fixing violations before work continues. Contractor may charge you extra or walk away.</p>
<p><strong>If contractor pulled permit:</strong> Contractor fixes issues immediately and reschedules inspection at their expense.</p>
<h3>Inspection 2: Dry-In Inspection</h3>
<p><strong>Occurs after underlayment installed, before final roofing material.</strong></p>
<p><strong>Inspector verifies:</strong></p>
<p>✓ Proper underlayment type installed (synthetic or felt, per NOA)</p>
<p>✓ Correct overlap (6&quot; side laps, 6&quot; end laps minimum)</p>
<p>✓ Proper fastener spacing per manufacturer specs</p>
<p>✓ Drip edge installed correctly (under underlayment on eaves, over on rakes)</p>
<p>✓ Valley underlayment properly installed</p>
<p>✓ No tears or damage in underlayment</p>
<p>✓ Product matches approved NOA</p>
<p><strong>Common failures:</strong></p>
<p>✗ Wrong underlayment product (not approved for HVHZ)</p>
<p>✗ Incorrect overlap or fastening pattern</p>
<p>✗ Drip edge installed backwards</p>
<p>✗ Tears or wrinkles in underlayment</p>
<p>✗ Missing valley reinforcement</p>
<p><strong>If you pulled the permit:</strong> You need to understand why it failed and ensure corrections are made properly. Many contractors will blame you for &quot;requiring cheap materials&quot; even though they installed wrong products.</p>
<p><strong>If contractor pulled permit:</strong> Their problem to fix immediately. They lose money on delays, so they do it right the first time.</p>
<h3>Inspection 3: Final Inspection</h3>
<p><strong>Occurs after all roofing material installed and flashing completed.</strong></p>
<p><strong>Inspector verifies:</strong></p>
<p>✓ Roofing material matches approved NOA</p>
<p>✓ Proper fastener pattern per manufacturer specs (specific to HVHZ)</p>
<p>✓ Enhanced fastening in perimeter zones (HVHZ requirement)</p>
<p>✓ All flashings properly installed (step flashing, counter flashing, valleys)</p>
<p>✓ Proper ventilation installed and functional</p>
<p>✓ No exposed fasteners or sealant dependence</p>
<p>✓ Clean workmanship and no visible defects</p>
<p>✓ Photo documentation for building department records</p>
<p><strong>Common failures:</strong></p>
<p>✗ Insufficient fasteners (didn&#39;t follow HVHZ pattern)</p>
<p>✗ Wrong fastener locations or spacing</p>
<p>✗ Improper flashing installation</p>
<p>✗ Inadequate ventilation</p>
<p>✗ Exposed fasteners requiring sealant</p>
<p>✗ Poor workmanship (wrinkles, misalignment)</p>
<p><strong>If you pulled the permit:</strong> Inspector issues correction notice to YOU. You&#39;re responsible for getting contractor back to fix issues (if they even answer your calls). No certificate of completion until fixed.</p>
