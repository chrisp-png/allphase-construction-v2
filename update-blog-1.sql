<p>✓ Pre-deliver all materials day before (no delays waiting for supplies)
✓ Stage materials for rapid installation (minimize roof exposure time)
✓ Have backup materials on site (avoid mid-project supply runs)</p>
<h3>Working in Controlled Sections</h3>
<p><strong>Strategic approach:</strong></p>
<p><strong>Inexperienced contractors:</strong> Tear off entire roof at once (faster for them, disaster for you)</p>
<p><strong>Experienced contractors:</strong> Work in 10x10 or 15x15 foot sections</p>
<p><strong>Section-by-section process:</strong></p>
<p><strong>Day 1, Section 1:</strong></p>
<ul>
<li>Tear off old roofing (200-300 sq ft section)</li>
<li>Inspect and repair deck as needed</li>
<li>Install new underlayment and base sheet</li>
<li>Install new roofing material (shingles, tile, or metal)</li>
<li><strong>Complete and watertight before lunch</strong></li>
</ul>
<p><strong>Day 1, Section 2:</strong></p>
<ul>
<li>Repeat process for adjacent section</li>
<li>Complete and watertight before end of day</li>
</ul>
<p><strong>Day 2-5:</strong> Continue section-by-section until entire roof complete</p>
<p><strong>Benefits of this approach:</strong></p>
<p>✓ Minimal area exposed at any time (limits dust, water risk, temperature loss)
✓ Most of home remains protected throughout project
✓ Allows rapid response to weather changes (quickly secure small exposed section)
✓ Easier to manage debris containment</p>
<p><strong>Logistics requirement:</strong> This approach requires more time and costs more (10-15% labor premium) but is essential for exposed ceiling homes.</p>
