<h3>Proper Ventilation and Insulation Knowledge</h3>
<p><strong>Critical for exposed ceiling homes:</strong></p>
<p><strong>Two primary approaches for vented vs unvented roof assemblies:</strong></p>
<p><strong>Option 1: Unvented (Hot Roof) Design</strong></p>
<p>How it works:</p>
<ul>
<li>Spray foam insulation applied directly to underside of roof deck</li>
<li>Creates air and vapor barrier</li>
<li>No ventilation required (sealed system)</li>
<li>Roof deck becomes part of conditioned space</li>
</ul>
<p><strong>Advantages:</strong></p>
<ul>
<li>Simpler installation (no baffles or vent channels required)</li>
<li>Maximum insulation R-value (no air gaps)</li>
<li>Eliminates roof deck temperature fluctuations</li>
</ul>
<p><strong>Requirements:</strong></p>
<ul>
<li>Must use closed-cell spray foam (minimum R-30)</li>
<li>Building code approval required (not all jurisdictions allow)</li>
<li>Higher cost ($3-5 per sq ft more than vented systems)</li>
</ul>
<p><strong>Option 2: Vented Roof Design</strong></p>
<p>How it works:</p>
<ul>
<li>Install baffles/channels creating 2&quot; air space above insulation</li>
<li>Soffit vents at eaves, ridge vents at peak</li>
<li>Air flows from soffit to ridge, ventilating deck</li>
<li>Insulation installed below air channel</li>
</ul>
<p><strong>Advantages:</strong></p>
<ul>
<li>Lower cost than spray foam</li>
<li>Traditional design familiar to all contractors</li>
<li>Removes moisture from roof deck assembly</li>
</ul>
<p><strong>Requirements:</strong></p>
<ul>
<li>Proper baffle installation (must maintain air channel)</li>
<li>Adequate soffit and ridge vent sizing</li>
<li>Insulation must not compress (lose R-value)</li>
</ul>
<p><strong>Experienced contractors understand local building officials&#39; preferences and can guide homeowners to code-compliant solutions.</strong></p>
<p><strong>Inexperienced contractors may not even understand these requirements exist — leading to permit rejections and project delays.</strong></p>
